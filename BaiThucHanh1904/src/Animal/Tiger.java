package Animal;

public class Tiger extends Animal{
    public void eat(){
        System.out.println("Tiger is eating...");
    }

    public void sleep(){
        System.out.println("Tiger is sleeping...");
    }

    public void walk(){
        System.out.println("Tiger is coming...");
    }

    public void run(){
        System.out.println("Tiger is running...");
    }

    public void roar(){
        System.out.println("Gawww");
    }
}
